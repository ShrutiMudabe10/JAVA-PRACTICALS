      private static double operation(double a,double b, char ch){
         if(ch=='/')return b/a;
         else if(ch=='*') return b*a;
         else if(ch=='+') return b+a;
         else return b-a;
    }
    private static int precedence(char ch){
          if(ch=='/' || ch=='*'){
              return 2;
          }else if(ch=='+'||ch=='-'){
              return 1;
          }
       return 0;    
    }
    private static double expressionEval(String exp){
        Stack<Double>st1 = new Stack<>();// storing operands
        Stack<String> st2 = new Stack<>();//storing operators
         String express[] =exp.splitWithDelimiters("[*/+-]",-1);
        for(int i=0; i<express.length; i++){
            String ch = express[i];
            
            if(ch.equals("+")||ch.equals("*")||ch.equals("/")||ch.equals("-")){
               while(!st2.isEmpty() && precedence(ch.charAt(0))<=precedence(st2.peek().charAt(0))){
                    double op1 = st1.pop();
                    double op2 = st1.pop();
                    char opr = st2.pop().charAt(0);
                    st1.push(operation(op1,op2,opr));
                 }
                 st2.push(ch);
            
            }else {
                st1.push(Double.parseDouble(ch));
            }
        }
        while(st2.size()>0){
             double op1 = st1.pop();
             double op2 = st1.pop();
             char opr = st2.pop().charAt(0);
           st1.push(operation(op1,op2,opr));
        }
      return st1.pop();  
    }
